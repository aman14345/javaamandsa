package com.aman;

public class BasicsLoop {
    public static void main(String[] args) {
        // if loop

        if(true)
       {
            System.out.println("hello world");
       }
        //While Loop
        int count =1;
        while(count !=5)
        {
            System.out.println(count);
            count++;
        }
        //For loop
        for(int i=1;i !=5;i++)
        {
            System.out.println(i);
        }
    }
}
